/**
 * Poll a single embedding task. Default interval is 2s while the task is
 * still running and stops automatically on terminal states (SUCCESS,
 * FAILURE, REVOKED). Mirrors the pattern used by `useReconTask`.
 */
export function useEmbeddingsTask(taskId: string | null, pollMs = 2_000) {
  const sub = useSub()
  return useQuery({
    queryKey: taskId ? ["recon", sub, "embeddings", "task", taskId] : ["recon", sub, "embeddings", "task", "none"],
    queryFn: () => reconApi.embeddingsTask(taskId as string),
    enabled: !!sub && !!taskId,
    refetchInterval: (q) => {
      const s = q.state.data?.state?.toUpperCase?.()
      if (!s) return pollMs
      if (s === "SUCCESS" || s === "FAILURE" || s === "REVOKED") return false
      return pollMs
    },
  })
}

/**
 * List recent embedding jobs (active + historical). Defaults mirror what
 * the backend suggests (25 rows, all kinds, include_active=1) so the page
 * shows a unified history+live view.
 */
export function useEmbeddingsJobs(
  params?: { limit?: number; status?: string; kind?: string; include_active?: 0 | 1 },
  pollMs?: number,
) {
  const sub = useSub()
  return useQuery({
    queryKey: ["recon", sub, "embeddings", "jobs", params ?? {}],
    queryFn: () =>
      reconApi.embeddingsJobs({
        limit: params?.limit ?? 25,
        status: params?.status ?? "any",
        kind: params?.kind ?? "embeddings",
        include_active: params?.include_active ?? 1,
      }),
    enabled: !!sub,
    refetchInterval: pollMs,
  })
}
