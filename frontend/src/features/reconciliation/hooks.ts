export function useReconTask(id: number | null, pollMs?: number) {
  const sub = useSub()
  return useQuery({
    queryKey: id ? qk.task(sub, id) : ["recon", sub, "tasks", "none"],
    queryFn: () => reconApi.getTask(id as number),
    enabled: !!sub && id != null,
    refetchInterval: (q) => {
      const data = q.state.data
      if (!data) return pollMs ?? false
      if (data.status === "running" || data.status === "queued") return pollMs ?? 2000
      return false
    },
  })
}

