const qk = {
  tasks: (sub: string) => ["recon", sub, "tasks"] as const,
  task: (sub: string, id: number) => ["recon", sub, "tasks", id] as const,
  queued: (sub: string) => ["recon", sub, "tasks", "queued"] as const,
  configs: (sub: string) => ["recon", sub, "configs"] as const,
  pipelines: (sub: string) => ["recon", sub, "pipelines"] as const,
  unreconciled: (sub: string, params: unknown) => ["recon", sub, "unreconciled", params] as const,
  balances: (sub: string, params: unknown) => ["recon", sub, "balances", params] as const,
}

function useSub() {
  const { tenant } = useTenant()
  return tenant?.subdomain ?? ""
}

